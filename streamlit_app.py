import streamlit as st

st.title("Igualdade de gênero: Oque é, Como combater a desigualdade de gênero.")
st.image("imagens/Ft1")
st.header("Igualdade de gênero")

st.subheader("Oque é?")
st.markdown("""
 A igualdade de gênero é a garantia dos direitos, responsabilidades e oportunidades iguais independente do gênero 
garantindo uma sociedade igual e justa independente do gênero.
            """)

st.subheader("Sua importância")
st.markdown("""
A importância da igualdade deve-se para a construção de uma sociedade melhor, contribuindo
para oportunidades iguais de emprego, educação, entre outros fatores, combatendo preconceitos e
reduzindo a violência baseada ne gênero que afeta milhares de mulheres até os dias de hoje.
Ela melhora o crescimento econômico, assim como a qualidade de vida.
            """)

st.subheader("Desigualdade de gênero")
st.markdown("""
A desigualdade de gênero é a diferença entre homens e mulheres, vindas de uma
cultura com raizes patriarcais, onde coloca mulheres em uma posição inferior,
afetando diversas áreas, causando a violência e machismo contra essas mulheres.

Isso vem de ideias que colocam mulheres como seres inferiores desde e
promoção da dominação masculina, e como a sociedade classifica e julga ambos os gêneros
colocando papeis e preconceitos.
            """)

st.subheader("Como afeta a sociedade")
st.markdown("""
A desigualdade de gênero afeta a sociedade em sua organização, colocando diferenças
entre salários, educação, impedindo oportunidades, gerando injustiças sociais, violência e criando
esteriotipos discriminátorios, limitando o potencial humano e o desenvolvimento
econômico do país
            """)

st.subheader("Como combater a desigualdade de gênero")
st.markdown("""
Para o combate da desigualdade de gênero á diversos fatores a serem debatido e escolhidos
como a garantia de direitos iguais atraves de leis e políticas publicas, 
a descontrução de esteriotipos dentro da sociedade, pela educação e conscientização,
garantir oportunidades para meninas e mulheres em áreas de liderança e oportunidades
de empregos melhores e , mudanças culturais pela valorização da igualdade
entre homens e mulheres.
            """)

